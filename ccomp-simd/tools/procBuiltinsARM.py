def nopsInstr(instr):
  """ returns the number of arguments appected by instruction 
     (returns None if instruction not found) """
  img = asm_dict.get(instr.upper())
  nops = None
  if img:
    nops = sum([ True if x else False for x in img[0:4] ])
  return nops

