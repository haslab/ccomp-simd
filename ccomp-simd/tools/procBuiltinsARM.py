def printsg (tres, targs):
  res = printTy(tres)+", [" 
  res += ";".join(map(lambda x:printTy(x), targs)) +  "], false"
  return res

def immsInfer (targs, instr):
  """ infers position of immediate arguments ---
    searches the args of the assembly instruction for 'imm'
    and checks if the corresponding argument is an 'int' 
    (returns None if imm is not an int or asm-instruction
     was not found) """
  imms = []
  imms_comment = ""
  img = asm_dict.get(instr)
  if img:
    for i in range(4):
      if not all([not 'imm' in s for s in img[i]]):
        imms.append(i)
        if not ('int' in targs[-(nopsInstr(instr)-i)]):
          imms = imms[:-1]
          imms_comment = "obs:immediate argument was rejected because of its type!"
  else:
    imms_comment = "cannot infer immediate arguments for " + instr
  return imms, imms_comment

def twoaddrOp (instr, nargs):
  """ infers if instruction is a two-address builtin """
  img = asm_dict.get(instr.upper())
  nops = nopsInstr(instr)
  twoaddr = "false"
  twoaddr_comment = ""
  if nops!=None:
    if nops == nargs:
      twoaddr = "true"
    else:
      if nops != nargs+1:
        twoaddr_comment = "INCONSISTENT NUMBER OF ARGUMENTS IN " + instr
  else:
    twoaddr_comment = "Cannot infer TWO-ADDR for instruction " + instr
  return twoaddr, twoaddr_comment

def switchasm(x):
  """ switch-case replacement for specific asm instructions """
  return {
    'LOADUPS': "MOVUPS (%1), %0",
    'STOREUPS': "MOVUPS %2, (%1)",
    'LOADSS': "MOVSS (%1), %0",
    'STORESS': "MOVSS %2, (%1)",
    'LOADUPD': "MOVUPD (%1), %0",
    'STOREUPD': "MOVUPD %2, (%1)",
    'LOADDQU': "MOVDQU (%1), %0",
    'STOREDQU': "MOVDQU %2, (%1)",
    'LOADHPD': "MOVHPD (%2), %1",
    'LOADLPD': "MOVLPD (%2), %1",
    'STOREHPD': "MOVHPD %2, (%1)",
    'STORELPD': "MOVLPD %2, (%1)",
  }.get(x, None)

def asmInstr (name):
  """ infers the instruction name """
  asm = (name.split('_')[-1]).upper()
  if asm_dict.get(asm): return asm
  if switchasm(asm): return switchasm(asm)
  if asm.endswith('128'): 
    temp = asm[:-3]
    if asm_dict.get(temp): return temp
  if asm.endswith('I128'): 
    temp = asm[:-4]
    if asm_dict.get(temp): return temp
  pos = asm.find('64')
  if pos != -1:
    temp = asm[:pos] + asm[pos+2:]
    if asm_dict.get(temp): return temp
  return asm

