asm_dict = dict([])

def asm_add(dic, name, op1, op2, op3, op4, opclass):
  img = dic.get(name)
  if img:
    op1 = img[0] + [op1] if op1 else img[0]
    op2 = img[1] + [op2] if op2 else img[1]
    op3 = img[2] + [op3] if op3 else img[2]
    op4 = img[3] + [op4] if op4 else img[3]
    opclass = img[4] + [opclass] if not (opclass in img[4]) else img[4]
  else:
    op1 = [op1] if op1 else []
    op2 = [op2] if op2 else []
    op3 = [op3] if op3 else []
    op4 = [op4] if op4 else []
    opclass = [opclass] if opclass else []
  dic[name] = (op1,op2,op3,op4,opclass)

